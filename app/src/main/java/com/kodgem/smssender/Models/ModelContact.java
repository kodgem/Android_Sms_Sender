package com.kodgem.smssender.Models;

public class ModelContact {

    private String name;
    private String number;
    private String numberEdit;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getNumberEdit() {
        return numberEdit;
    }

    public void setNumberEdit(String numberEdit) {
        this.numberEdit = numberEdit;
    }
}
